public class math {
    public static void main(String[] args){

        double result; 

        result = Math.pow(2,5);
        System.out.println(result);
        result = Math.abs(-5);
        System.out.println(result);
        result = Math.sqrt(4);
        System.out.println(result);
        result = Math.round(3.29);
        System.out.println(result);
        result = Math.ceil(4.23);
        System.out.println(result);
        result = Math.floor(4.99);
        System.out.println(result);
        result = Math.max(4, 90);
        System.out.println(result);
    }
}
